	function addItem(product) {
		const productImg = product.querySelector('img').getAttribute('src'),
			productTitle = product.querySelector('.item-product__title').innerHTML,
			productSubTitle = product.querySelector('.item-product__text').innerHTML,
			productPrice = product.querySelector('.item-product__prices_main').innerHTML,
			productId = product.dataset.pid,
			listParent = document.querySelector('.cart-list__items'),
			newItem = `
		<li data-pidl="${productId}" class="cart-list__item">
			<a href="" class="cart-list__img _ibg"><img src="${productImg}" alt=""></a>
			<div class="cart-list__body">
				<a href="" class="cart-list__title-item">${productTitle}</a>
				<div class="cart-list__subtitle">${productSubTitle}</div>
				<div class="cart-list__price">${productPrice}</div>
				<div class="quantity">
					<div class="quantity__button quantity__button_minus"></div>
					<div class="quantity__input">
						<input autocomplete="off" readonly type="text" name="form[]" value="1">
					</div>
					<div class="quantity__button quantity__button_plus"></div>
				</div>
				<a href="" class="cart-list__delete">Удалить</a>
			</div>
		</li>
		`;
		listParent.insertAdjacentHTML('beforeend', newItem);
		const anotherItem = listParent.querySelector(`[data-pidl="${productId}"]`);
		const listener = (e) => {
			const target = e.target;
			if (target.classList.contains('quantity__button_minus')) {
				const input = target.closest('.quantity').querySelector('input');
				if (input.value > 1) {
					input.value = --input.value;
					calculate();
				}
			} else if (target.classList.contains('quantity__button_plus')) {
				const input = target.closest('.quantity').querySelector('input');
				input.value = ++input.value;
				calculate();
			} else if (target.classList.contains('cart-list__delete')) {
				e.preventDefault();
				anotherItem.removeEventListener('click', listener);
				target.closest('.cart-list__item').remove();
				checkShow();
				calculate();
			}
		}
		anotherItem.addEventListener('click', listener);
		calculate();
	}

	function calculate() {
		const totalPrice = document.querySelector('.cart-list__totalPrice span');
		const iconCart = document.querySelector('.cart-header__icon');
		let allPrice = 0;
		let allQuantity = 0;
		productList = document.querySelectorAll('.cart-list__item');
		productList.forEach(item => {
			const price = parseInt(item.querySelector('.cart-list__price').innerHTML.replace(/\D+/g, ""));
			const quantity = +item.querySelector('input').value;
			allPrice = allPrice + (price * quantity);
			allQuantity = allQuantity + quantity;
		});
		allPrice = allPrice.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ".");
		totalPrice.innerHTML = allPrice;

		if (productList.length > 0) {
			if (allQuantity === 1) {
				iconCart.innerHTML = '<span>1</span>';
			} else {
				iconCart.querySelector('span').innerHTML = allQuantity;
			}
		} else {
			iconCart.innerHTML = '';
		}
	}

