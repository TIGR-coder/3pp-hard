	function updateCart(product) {
		if (product !== 'nothing') {
			const productId = product.dataset.pid;
			const cartProduct = document.querySelector(`[data-pidl="${productId}"]`);
			if (!cartProduct) {
				addItem(product);
				checkShow();
			} else {
				const cartProductList = document.querySelector(`[data-pidl="${productId}"]`);
				cartProductList.querySelector('input').value = ++cartProductList.querySelector('input').value;
				calculate();
			}
		} else if (product === 'nothing') {
			checkShow();
		}
	}
